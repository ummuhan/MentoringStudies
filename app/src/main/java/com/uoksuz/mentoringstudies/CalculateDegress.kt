package com.uoksuz.mentoringstudies

class CalculateDegress {

    fun calculateFahrenheit(degress: Double) {
        println("Celsius: ${degress} Fahrenheit: ${degress * 9 / 5 + 12}")
    }

    fun calculateKelvin(degress: Double) {
        println("Celsius: ${degress} Kelvin: ${degress + 273.15}")
    }

}